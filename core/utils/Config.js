// ----------------------------------------
// PACKAGE IMPORTS
// ----------------------------------------
import Config from "@app-configs/App";
import Themes from "@app-themes";


/*
 * ---------------------------------------------------------------------------------------
 * ---------------------------------------------------------------------------------------
 *  METHODS
 * ---------------------------------------------------------------------------------------
 * ---------------------------------------------------------------------------------------
 */

// ----------------------------------------
// MAIN METHODS
// ----------------------------------------

  const paddingHorizontal = (mul = 1) => base(2 * mul);

// ----------------------------------------

  const theme = (key = "") => {
    let data = Themes[Config.theme];

    const keys = key.split(".");

    for (let x in keys) {
      if (!data[keys[x]]) {
        return null;
      }

      data = data[keys[x]];
    }

    return data;
  };

// ----------------------------------------

  const colors = () => theme("Color.BASE");

// ----------------------------------------

  const base = (mul = 1) => (mul * theme("Size.BASE"));


/*
 * ---------------------------------------------------------------------------------------
 * ---------------------------------------------------------------------------------------
 *  EXPORT
 * ---------------------------------------------------------------------------------------
 * ---------------------------------------------------------------------------------------
 */

export default {
	paddingHorizontal,
	theme,
	base,
  colors,
};
