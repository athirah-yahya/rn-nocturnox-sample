  const isValid = (errors) => {
    for (let x in errors) {
      if (!!errors[x]) {
        return false;
      }
    }

    return true;
  };

// ----------------------------------------

  const empty = (value) => (value !== undefined) && (value !== null) && (value !== "");

// ----------------------------------------

  const email = (value) => {
      var re = /^(([^<>()\[\]\\.,;:\s@"]+(\.[^<>()\[\]\\.,;:\s@"]+)*)|(".+"))@((\[[0-9]{1,3}\.[0-9]{1,3}\.[0-9]{1,3}\.[0-9]{1,3}\])|(([a-zA-Z\-0-9]+\.)+[a-zA-Z]{2,}))$/;

      return re.test(String(value).toLowerCase());
  };


