import {
  Spacer,
} from "@core-components-enhancers";




/*
 * ---------------------------------------------------------------------------------------
 * ---------------------------------------------------------------------------------------
 *  MAIN CLASS
 * ---------------------------------------------------------------------------------------
 * ---------------------------------------------------------------------------------------
 */
export default class Lister extends Component {

// ----------------------------------------
// ----------------------------------------
// CONSTRUCTOR AND LIFE CYCLES
// ----------------------------------------
  
  shouldComponentUpdate(nextProps, nextState) {
    if (this.props !== nextProps) {
      return true;
    }

    return false;
  }


// ----------------------------------------
// ----------------------------------------
// METHODS
// ----------------------------------------

  getListItems() {
    const {
      children,
      separatorStyle,
      space,
    } = this.props;

    if (!children.length) {
      return children;
    }

    const itemRenders = [];
    children.map((item, index) => {
      itemRenders.push(item);

      if (index < children.length - 1) {
        itemRenders.push(
          <Spacer space={ space } style={ [separatorStyle] } key={ `spacer-${index}` }/>
        );
      }
    });

    return itemRenders;
  }


// ----------------------------------------
// ----------------------------------------
// RENDERS
// ----------------------------------------  

// ----------------------------------------
// ----------------------------------------
// MAIN RENDER
// ----------------------------------------

  render() {
    return (
      <Spacer>
        { this.getListItems() }
      </Spacer>
    );
  }

}