// ----------------------------------------
// PACKAGE IMPORTS
// ----------------------------------------
import React, { Component } from "react";

// ----------------------------------------
// LOCAL & CONFIG IMPORTS
// ----------------------------------------
import Styles from "./style";

// ----------------------------------------
// COMPONENT IMPORTS
// ----------------------------------------
import {
  WrapperCircledIcon,
} from "@app-components-core/Wrapper";
import {
  Spacer,
} from "@core-components-enhancers";
import Card from "@app-components-intermediaries/Card";




/*
 * ---------------------------------------------------------------------------------------
 * ---------------------------------------------------------------------------------------
 *  MAIN CLASS
 * ---------------------------------------------------------------------------------------
 * ---------------------------------------------------------------------------------------
 */
export default class CardIconed extends Component {

// ----------------------------------------
// ----------------------------------------
// CONSTRUCTOR AND LIFE CYCLES
// ----------------------------------------

  shouldComponentUpdate(nextProps, nextState) {
    if (this.props !== nextProps) {
      return true;
    }

    return false;
  }


// ----------------------------------------
// ----------------------------------------
// RENDERS
// ----------------------------------------

  _renderIcon() {
    const {
      icon,
      iconBackgroundColor,
    } = this.props;

    return (
      <WrapperCircledIcon color={ iconBackgroundColor }>
        { icon }
      </WrapperCircledIcon>
    );
  }


// ----------------------------------------
// ----------------------------------------
// MAIN RENDER
// ----------------------------------------

  render() {
    return (
      <Card
        theme={ "Base" }
        { ... this.props }
      >
        <Spacer row>
          { this._renderIcon() }

          <Spacer horizontal space={ 1.5 }/>

          <Spacer flex={ 1 } top={ .5 }>
            { this.props.children }
          </Spacer>
        </Spacer>
      </Card>
    );
  }


// ----------------------------------------

}
