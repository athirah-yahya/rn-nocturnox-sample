import {
  DataService,
  MapperService,
} from "@core-services";

// ----------------------------------------
// EVENT IMPORTS
// ----------------------------------------
import {
  SAMPLE,
} from "@app-events";


/*
 * ---------------------------------------------------------------------------------------
 * ---------------------------------------------------------------------------------------
 *  METHODS
 * ---------------------------------------------------------------------------------------
 * ---------------------------------------------------------------------------------------
 */
export function getList(data = {},onActionComplete:func) {
  return DataService.request(
    "fetchSampleList",
    {},
    (response, dispatch) => {
      dispatch({
        type: SAMPLE.SET_LIST,
        data: {
          samples: MapperService.mapResponseList("sampleDetail", response.samples),
        },
      });
    }
  );
}

// ----------------------------------------

export function getDetail(data = {}, onActionComplete:func) {
  return DataService.direct(
    "getSampleDetail",
    {},
    (response, dispatch) => {
      dispatch({
        type: SAMPLE.SET_BASE,
        data: {
          base: data,
        },
      });
    }
  );
}
