export function fetchSampleList() {
  const data = require("@app-data-dummies/samples");

  return {
    samples: data,
  };
}

// ----------------------------------------

export function getSample(param) {
  let data = require("@app-data-dummies/samples");

  data = data.filter(item => item.record_id === param.record_id);

  return {
    sample: data,
  };
}
